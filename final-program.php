<h3 class="title">Nanomaterials, Nanodevices: Fabrication, Characterization and Application I<br>
<small>Session Chair: Dr. Arzu Sardarli, First Nations University of Canada, Canada</small></h3>
<br>


<div class="panel-group" id="accordion" role="tablist" aria-multiselectable="true"> <!-- Paper Group Starts-->


<div class="panel panel-default"><!-- Individual Paper Starts-->
    <div class="panel-heading" role="tab" id="heading111">
      <h4 class="panel-title">
        <a  class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse111" aria-expanded="false" aria-controls="collapse111">Synthesis and Characterization of Nanocomposite of Functionalized Graphene Oxide with Multi Core-Shell Fe<sub>3</sub>O<sub>4</sub>-ZnO-TiO<sub>2</sub> Nanoparticles<br></a>
          <small>Ali Nemati, Central Michigan University & Sharif University of Technology, USA</small>
      </h4>
    </div>

    <div id="collapse111" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading111">
      <div class="panel-body">
        <dl class="dl-horizontal">
          <dt>Time</dt>
          <dd>11:15 - 11:25</dd>
          <dt>Authors</dt>
          <dd>Ali Nemati, Waseem Haider, S. Ghanbarnezhad, Zia Ur Rahman, Syed Nabeel Ahmed</dd>
           <dt>Abstract</dt>
          <dd>This project involves development and characterization of nanocomposite of functionalized graphene oxide with multi core-shell Fe<sub>3</sub>O<sub>4</sub>-ZnO-TiO<sub>2</sub> nanoparticles (abbreviated as G-MCSN) with enhanced multi-functional properties. The multi core-shell nanoparticles consist of superparamagnetic core with modified shells of photocatalytic materials. After synthesis of Fe<sub>3</sub>O<sub>4</sub> nanoparticles (using co-precipitation and solvothermal processes), it was coated with multi shells of ZnO and TiO<sub>2</sub> (via a facile sol–gel rout). Functionalized graphene oxide was synthesized and mixed with multi core-shell nanoparticles to prepare the nanocomposite. The phases and morphology of the core-shell nanoparticles and the nanocomposite were investigated by X-ray diffraction (XRD), field emission scanning electron microscopy (FE/SEM) and transmission electron microscopy (TEM/SAED). The magnetization hysteresis curves of the samples were conducted by a vibrating sample magnetometer (VSM). <br>
          The results showed that the average size of synthesized magnetite nanoparticles were less than 20 nm. The précised control of the procedure resulted in the formation of core-shell structure after coating with ZnO and TiO<sub>2</sub>. The median size of core-shell nanoparticles was less than 50 nm. TEM studies revealed the formation of a few layers functionalized graphene oxide (FGO), as well as core-shell nanoparticles. Vibrating sample magnetometer measurement showed that magnetic property of the core-shell nanoparticles was lower than the bare Fe3O4 nanoparticles (from 40.96 emu.g<sup>-1</sup> in bare Fe<sub>3</sub>O<sub>4</sub> nanoparticles to 20.13 and 12.98 emu.g<sup>-1</sup> in core-shell nanoparticles). But, when nanocomposite was made (FGO with core-shell nanoparticles), magnetic property was increased in comparison with the core-shell nanoparticles from 12.98 emu.g<sup>-1</sup> to 28.18 emu.g<sup>-1</sup>.</dd>
          <dt>Keywords</dt>
          <dd>Functionalized graphene oxide, core-shell nanoparticles, Fe<sub>3</sub>O<sub>4</sub>-ZnO-TiO<sub>2</sub>, TEM, VSM.</dd>
        </dl>
      </div>
    </div>
