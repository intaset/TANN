<h6 class="info">9:50 AM - 10:40 AM | Session Chair: Dr. Jin Zhang, University of Western Ontario, Canada</h6>
<h3 class="title">Combination of Nanomaterials and Organic Semiconductors for Electronic and Optoelectronic Device Fabrication<br>
<small><a href="http://www.nanoqam.uqam.ca/professeur.php?id=3&lang=en" target="_blank">Dr. Ricardo Izquierdo, Ecole de Technologie Superieure, Canada</a></small></h3>
<br>

<dl class="dl-horizontal">
  <dt>Abstract</dt>
  <dd>The use of a combination of nanomaterials and organic semiconductors offers a great flexibility for the fabrication of various electronic and optoelectronic devices. In this talk, we will present various types of device, which were obtained by using such a combination. First device is an organic memory consists of two aluminium electrodes separated by a layer from an organic semiconductor that contains gold nanoparticles supported by parylene pillars. A second example is the use of carbon nanotubes or graphene for semi-transparent conducting electrodes for electroluminescent organic diodes (OLEDs). We will also discuss about the efficiency enhancement of a standard bulk heterojunction (BHJ) solar cell by the incorporation of various nanomaterials such as a nanohybrid composite material made of lead sulfide (PbS) colloidal quantum dots (QDs) and multiwall carbon nanotubes (MWCNTs) or by the incorporation of gold nanorods. Finally, the combination of a semitransparent electrochemical sensor, made from silver nanowires, and a fluorescence sensor, made from organic optoelectronic components, in order to build a sensing platform for water toxicity evaluation, will be presented.</dd>
</dl>
