        <a  class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse123" aria-expanded="false" aria-controls="collapse123">Phosphorene and Graphene Nanoribbons with Vacancies in Magnetic Field<br></a>
          <small>Jan  Smotlacha, Joint Institute for Nuclear Research, Russian Federation</small>
      </h4>
    </div>

    <div id="collapse123" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading123">
      <div class="panel-body">
        <dl class="dl-horizontal">
          <dt>Time</dt>
          <dd>11:45 - 12:05</dd>
          <dt>Authors</dt>
          <dd>Jan Smotlacha, Richard Pincak</dd>
           <dt>Abstract</dt>
          <dd>The electronic spectra of the nanostructured materials show an interesting behaviour under the influence of the uniform magnetic field: the dependence of the energy levels on the magnetic field shows a fractal structure. This feature follows from the procedure of the calculation in which the matrix form of the Schrödinger equation is replaced by a system of the Harper equations, where the exponentials with the magnetic factors are supplied to all of the terms. We show the electronic spectra for the case of the narrow phosphorene and graphene zigzag nanoribbons with atomic vacancies and for the nanoribbons equipped with the so-called Stone-Wales defects. </dd>
          <dt>Keywords</dt>
          <dd>Graphene nanoribbons, phosphorene, electronic spectrum, magnetic field, density of states, edge states, Stone-Wales defects</dd>
        </dl>
