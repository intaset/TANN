<p class="legend">Keynote Lecture</p>
<h6 class="info">9:15 AM - 10:05 AM | Session Chair: Dr. Jin Zhang, University of Western Ontario, Canada</h6>
<h3 class="title">Nanoengineering New Multifunctional Contrast Agents for Medical Imaging of Cancer<br>
<small><a href="http://mse.utoronto.ca/faculty-staff/professors/matsuura/" target="_blank">Dr. Naomi Matsuura, University of Toronto, Canada</a></small></h3>
<br>

<dl class="dl-horizontal">
  <dt>Abstract</dt>
  <dd>Imaging is a fundamental tool in the practice of medicine. In parallel with the development of improved imaging systems and techniques, there is increasing interest in designing new contrast agents that can help guide and assess personalized treatment for cancer patients. A new opportunity in materials science is the development of new injectable materials that can be activated by medical imaging radiation under image guidance to treat cancer. This talk will focus on the development of new contrast agents that can facilitate more focused and targeted delivery of cancer therapies to tumours for higher therapeutic ratios, and can permit the treatment of hard-to-access organs like the brain in a minimally-invasive manner. Specific examples of different hierarchical and composite contrast agents that are assembled to address and balance biological and physical challenges of contrast agent development will be given, with a focus on the use of perfluorocarbon bubbles, droplets and nanoparticles as multifunctional contrast agents for ultrasound imaging and therapy applications.</dd>
</dl>
