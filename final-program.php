<a id="Development of Hetero-nanostructures for Advanced Biomedical Applications"></a>
<div class="section">
<p class="legend">Workshop</p>
<h6 class="info">1:05 PM - 3:05 PM</h6>
<h3 class="title">Development of Hetero-nanostructures for Advanced Biomedical Applications<br>
<small><a href="http://eng.uwo.ca/chemical/faculty/zhang_j/" target="_blank">Dr. Jin Zhang, University of Western Ontario, Canada</a></small></h3>
<br>

<dl class="dl-horizontal">
  <dt>Abstract</dt>
  <dd>Tailor-made nanosystem refers the integration of different components/materials at nanoscale which can show multi-functional and unique properties. Rational design and fabrication of such hetero-nanostructures are vital to break the boundaries of traditional technologies. One of Dr. Jin Zhang’s research interests lies in producing magnetic and optical hetero-nanostructures which show distinguish properties, and have been applied in various devices, e.g. data storage, sensors, and targeted drug delivery, etc. It is quite challenge to control the interface of magnetic and non-magnetic materials to precisely gain the desired properties. Here, Dr. Zhang will show different hetero-nanostructures with magnetic and optical properties (from zero-dimensional hetero-nanostructures to three dimensional hetero-nanostructures). In addition, incorporating proteins with nanomaterials can enhance protein’s stabilities, and may lead to new applications. This workshop will also focuse on the different interactions between proteins and designed hetero-nanostructures. The applications of the protein-conjugated hetero-nanostructures in non-invasive biosensor and targeted drug delivery will be discussed.</dd>
</dl>

