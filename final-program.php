      <h4 class="panel-title">
        <a  class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse103" aria-expanded="false" aria-controls="collapse103">Effects of a Dielectric Barrier Discharge (DBD) Treatment on Chitosan/Polyethylene Oxide Nanofibers and Their Cellular Interactions<br></a>
          <small>Mahtab Asadian, University of Ghent, Belgium</small>
      </h4>
    </div>

    <div id="collapse103" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading103">
      <div class="panel-body">
        <dl class="dl-horizontal">
          <dt>Time</dt>
          <dd>1:10 - 1:30</dd>
          <dt>Authors</dt>
          <dd>Mahtab Asadian, Heidi Declercq, Rino Morent, Nathalie De Geyter</dd>
          <dt>Abstract</dt>
          <dd>In this study, chitosan (CS)/polyethylene oxide (PEO) nanofibrous mats (Ø: 166 ± 43 nm) were prepared by electrospinning and subsequently subjected to a dielectric barrier discharge (DBD) treatment in the presence of three different gases (argon, ammonia/helium and nitrogen). Afterwards, water contact angle measurements, high-resolution X-ray photoelectron spectroscopy (XPS) and scanning electron microscopy (SEM) were used to characterize the surface properties of the CS/PEO nanofibers (NFs) before and after plasma treatment. Cell behaviour of the fibers was also evaluated by live/dead fluorescence microscopy one day and seven days after seeding of human foreskin fibroblasts (HFF) on the CS/PEO NFs. In addition, the morphology of the cells on the NFs was also visualized making use of SEM. <br>
          The obtained results revealed that the water contact angle on the CS/PEO NFs could be significantly decreased from approximately 59° to 10°, 26° and 43° after plasma modifications in NH3/He, N2 and Ar respectively. XPS results revealed that N2 and NH3/He plasma treatments result in the incorporation of nitrogen-containing functional groups while argon plasma treatment mainly result in a partial degradation of the CS/PEO polymer chains. The DBD treatments are thus able to strongly increase the hydrophilicity of the CS/PEO nanofibrous mats. In addition, it was also observed that cellular interactions on the CS/PEO NFs were also significantly increased due to these plasma treatments. Hence, an effective plasma-induced surface modiﬁcation of nanofibrous CS/PEO surfaces can signiﬁcantly improve the biocompatibility of CS/PEO. The best outcome of cell proliferation was noted for the argon plasma modification, which was even higher than the positive control sample although the hydrophilicity was the lowest in this case. The observations thus suggest that not only the hydrophilicity characteristics but also the presence of distinct functional groups on plasma treated CS/PEO NFs itself might have a significant influence on the observed enhanced cellular interactions. </dd>
          <dt>Keywords</dt>
          <dd>Electrospinning, chitosan/PEO nanofibers, non-thermal plasma treatment, cell interactions</dd>
        </dl>
