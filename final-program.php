<p class="legend">Keynote Lecture</p>
<h6 class="info">9:00 AM - 9:50 AM | Session Chair: Dr. Jin Zhang, University of Western Ontario, Canada</h6>
<h3 class="title">Nanoscale Heat Transport and Magnetism in Magnetic Data Storage Applications<br>
<small><a href="http://lassonde.yorku.ca/users/simonepisana" target="_blank">Dr. Simone Pisana, York University, Canada</a></small></h3>
<br>

<dl class="dl-horizontal">
  <dt>Abstract</dt>
  <dd>The hard disk drive has been a key technology in computing for over 60 years, and in spite of the advent of solid state drives which now dominate consumer products, advances in nanoscience are making their way into the hard disk drive to continue to support the demands of cloud computing. In this talk I will outline heat-assisted magnetic recording (HAMR), a technology coming into production soon that allows data storage densities to go beyond what was thought to be the unbreakable superparamagnetic limit of 1 Tera-bit per square inch. Specifically, I will focus on some of the nanoscale aspects of heat transport and magnetism that have posed challenges in the development of HAMR. Regarding heat transport, I will discuss heat dissipation issues in nanostructures such as the near-field plasmonic antenna used to record the data, and show that changes in phonon dispersion in layers only a couple nm in thickness can have a surprising impact on interfacial heat flow. In terms of magnetism, I will present the characterization of the distribution in Curie temperatures in the nanoscale ferromagnetic grains used to store the data. This optical pump-probe technique bypasses the difficulty of attempting to measure the vanishing magnetization at the ferromagnetic-paramagnetic transition, and opens up new questions in the physics of magnetization dynamics at high temperature.</dd>
</dl>
</div>
